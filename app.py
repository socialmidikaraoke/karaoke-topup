def get_google_sheet():
    scopes = ["https://www.googleapis.com/auth/spreadsheets"]
    # ‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ‡∏ï‡πâ‡∏≠‡∏á‡πÉ‡∏ä‡πâ‡πÑ‡∏ü‡∏•‡πå Key ‡∏Ç‡∏≠‡∏á Google Cloud ‡∏Ñ‡∏∏‡∏ì
